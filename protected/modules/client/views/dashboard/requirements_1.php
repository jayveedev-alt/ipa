                    <li>Bldg. Permit &/or Certificate of Occupancy of all existing buildings within subject</li>
                </ul>
                <ul>
                    <li>Barangay Clearance</li>
                    <li>DPWH (If located in national highway)</li>
                    <li>Home Owners Assoc. Clearance (for commercial establishment or in subdivision)</li>
                    <li>Fire Safety Evaluation Clearance (FSEC) & other fire safety clearances</li>
                    <li>Bldg. Permit &/or Certificate of Occupancy of all existing buildings within subject </li>
                    <li>EPNRO Clearance (For need to cut trees, etc.)</li>
                    <li>LLDA Clearance or LLMO Clearance (For car wash, laundry shop, restaurant, high occupancy buildings, etc.)</li>
                    <li>Environmental Compliance Certificate (ECC) from DENR (For Gen-set, etc.)</li>
                    <li>Height Clearance Permit / Height Limitation from CAAP (ATO) (For Building / Structure 4-Storey and above)</li>
                    <li>MMDA Clearance (If a lot is adjacent to a creek or river, etc.). DPWH Clearance (For Lot Fronting Highways, etc.)</li>
                    <li>HLURB Clearance (For Condominium Projects, etc.)</li>
                    <li>NGCP Clearance (For Lot near Transmission lines) DOH-BHDT Clearance (For Telecom Antenna, etc.)</li>
                    <li> Energy Regulatory Board (ERB), (For Gas Stations)</li>
                    <li>Seismic Accelerograph (For Buildings 50 meters and above, commercial area with 10,000.00 sq. m. gross floor area or 1,000 occupants, etc.)lot, if application is renovation/addition or extension</li>
                </ul>
            </li>
            <li class="mb-3">
                <strong>Building Plans</strong>
                <ul>
                    <li>Eight (8) sets of Construction plans/Blueprints</li>
                    <li>Lot plan/Location Plan</li>
                    <li>Architectural Plans</li>
                    <li>Structural Plans</li>
                    <li>Plumbing/Sanitary Plans</li>
                    <li>Electrical Plans</li>
                    <li>Fire Protection Plans</li>
                    <li>Electronics Plans</li>
                    <li>Mechanical Plans</li>
                </ul>
            </li>
            <li class="mb-3">
                <strong>Supporting Documents</strong>
                <ul>
                    <li>Photocopy of updated PTR / PRC ID / Accredited Professional Organization ID</li>
                    <li>Photocopy of owner’s valid ID</li>
                    <li>Certified True Copy of Transfer Certificate of Title (TCT) from Registry of Deeds Or photocopy of Deed of Absolute Sale</li>
                    <li>Three (3) sets Structural Calculation (For Building / Structure 2-Storey and above, warehouse and additional floors, etc.)</li>
                    <li>Three (3) sets Geo-technical Investigation Report by a DPWH Accredited Contractor (For Structure 3-Storey and above, for 2-storey with roof deck or basement)</li>
                    <li>PDF copy of Geo-technical Investigation Report, Structural Calculations in CD/DVD/USB (For Commercial, Industrial and Institutional Buildings, etc.)</li>
                    <li>PDF copy of Construction Plans / Blueprint in CD / DVD / USB (For Commercial, Industrial, Institutional Buildings, etc.)</li>
                    <li>Photocopy of Old Building Permits and Approved Construction Plans (For Renovation / Extension / Additional Floor)</li>
                    <li>Notarized Authorization from Lot owner to construct (If applicant is not the lot owner)</li>
                    <li>Notarized Neighbor’s Consent</li>
                </ul>
            </li>
